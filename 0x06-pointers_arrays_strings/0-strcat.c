/**
* _strcat - appends src to the dest string
* @dest: string to append by src
* @src: string to append to dest
* Return: addres of dest
*/
cha *_strat(char *dest, char *src)
{
int i, j;
i = j = 0;
while (*(dest + i))
{
i++;
}
while ((*(dest + i) = *(src + j)))
{
i++;
j++;
}
return (dest);
}
