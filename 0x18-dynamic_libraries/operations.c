#include "ops.h"

/**
 * add - adds two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiples two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns the remainder of division of two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
