#include <stdio.h>
/**
* main - Prints all arguments it receives
* @argc: The number of arguments supplied to the program.
* @argv: An array of pointers to the arguments.
* Return: Always 0.
*/
i main(int argc, char *argv[])
{
int arg;
for (arg = 0; arg < argc; arg++)
printf("%\n",argv[arg]);
return (0);
}
